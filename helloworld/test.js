//! <h1> Обчислення суми багатьох чисел </h1>
// Сума чисел від 1 до 100

var i, s; //-

 // Ініціалізуємо акамулятор
s=0;

 // Для всіх чисел від 1 до 100
for(i=1; i<=100; i++) {
  // Додаємо число до акамулятора
  s += i;
}

// Сума:
print(s);
